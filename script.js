/**
 * State Management
 */
let currentLang = 'ar';
let currentTheme = 'light';

// Load saved theme or system preference
const savedTheme = localStorage.getItem('theme');
if (savedTheme) {
    currentTheme = savedTheme;
} else if (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches) {
    currentTheme = 'dark';
}
document.documentElement.setAttribute('data-theme', currentTheme);

/**
 * DOM Elements
 */
const themeToggleBtn = document.getElementById('theme-toggle');
const langToggleBtn = document.getElementById('lang-toggle');
const i18nElements = document.querySelectorAll('[data-i18n]');
const htmlElement = document.documentElement;
const mobileToggleBtn = document.getElementById('mobile-toggle');
const navLinksContainer = document.querySelector('.nav-links');

/**
 * Helper: update theme icons visibility
 */
function updateThemeIcons() {
    if (!themeToggleBtn) return;
    const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
    const sunIcon = themeToggleBtn.querySelector('.sun-icon');
    const moonIcon = themeToggleBtn.querySelector('.moon-icon');
    if (sunIcon && moonIcon) {
        sunIcon.style.display = isDark ? 'inline-block' : 'none';
        moonIcon.style.display = isDark ? 'none' : 'inline-block';
    }
}

/**
 * Mobile Menu Toggle
 */
if (mobileToggleBtn && navLinksContainer) {
    mobileToggleBtn.addEventListener('click', () => {
        mobileToggleBtn.classList.toggle('active');
        navLinksContainer.classList.toggle('active');
        // Toggle close icon visibility
        const menuIcon = mobileToggleBtn.querySelector('.menu-icon');
        const closeIcon = mobileToggleBtn.querySelector('.close-icon');
        if (menuIcon && closeIcon) {
            const isActive = mobileToggleBtn.classList.contains('active');
            menuIcon.style.display = isActive ? 'none' : 'inline-block';
            closeIcon.style.display = isActive ? 'inline-block' : 'none';
        }
    });

    // Close mobile menu when a link is clicked
    document.querySelectorAll('.nav-link').forEach(link => {
        link.addEventListener('click', () => {
            mobileToggleBtn.classList.remove('active');
            navLinksContainer.classList.remove('active');
            const menuIcon = mobileToggleBtn.querySelector('.menu-icon');
            const closeIcon = mobileToggleBtn.querySelector('.close-icon');
            if (menuIcon && closeIcon) {
                menuIcon.style.display = 'inline-block';
                closeIcon.style.display = 'none';
            }
        });
    });
}

/**
 * Theme Toggle Logic
 */
if (themeToggleBtn) {
    themeToggleBtn.addEventListener('click', () => {
        currentTheme = currentTheme === 'light' ? 'dark' : 'light';
        document.documentElement.setAttribute('data-theme', currentTheme);
        localStorage.setItem('theme', currentTheme);
        updateThemeIcons();
    });
    updateThemeIcons();
}

/**
 * Language Toggle Logic
 */
function updateText() {
    i18nElements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.textContent = translations[currentLang][key];
        }
    });
    // Update document title
    if (translations[currentLang] && translations[currentLang].pageTitle) {
        document.title = translations[currentLang].pageTitle;
    }
}

if (langToggleBtn) {
    langToggleBtn.addEventListener('click', () => {
        if (currentLang === 'ar') {
            currentLang = 'en';
            htmlElement.setAttribute('dir', 'ltr');
            htmlElement.setAttribute('lang', 'en');
            langToggleBtn.textContent = 'عربي';
        } else {
            currentLang = 'ar';
            htmlElement.setAttribute('dir', 'rtl');
            htmlElement.setAttribute('lang', 'ar');
            langToggleBtn.textContent = 'English';
        }
        updateText();
    });
    // Set initial button text based on currentLang (ar default)
    langToggleBtn.textContent = 'English';
    updateText();
}

/**
 * FAQ Accordion
 */
const accordionItems = document.querySelectorAll('.accordion-item');
if (accordionItems.length) {
    accordionItems.forEach(item => {
        const header = item.querySelector('.accordion-header');
        if (header) {
            header.addEventListener('click', () => {
                const isActive = item.classList.contains('active');
                // Close all items
                accordionItems.forEach(acc => acc.classList.remove('active'));
                // Open clicked if it was not active
                if (!isActive) {
                    item.classList.add('active');
                }
            });
        }
    });
}

/**
 * Scroll Animations via IntersectionObserver
 */
const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.1
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('is-visible');
            observer.unobserve(entry.target); // stop observing after animation
        }
    });
}, observerOptions);

const animatedElements = document.querySelectorAll('.animate-on-scroll');
animatedElements.forEach(el => observer.observe(el));

